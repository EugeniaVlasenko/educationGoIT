package edu.modul_7;

public class Client {

    private String name;
    private int count;
    private String type;

    public Client(String name, int count, String type) {

        this.name = name;
        this.count = count;
        this.type = type;

    }

    public String getType() {

        return type;

    }

    public int getCount() {

        return count;

    }

}
